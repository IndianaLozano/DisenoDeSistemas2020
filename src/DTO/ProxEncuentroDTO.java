package DTO;

public class ProxEncuentroDTO {
	
	private String pLocal;
	private String pVisitante;
	private int idEncuentro;
	private String fecha;
	private String hora;
	
	
	public String getpLocal() {
		return pLocal;
	}
	public void setpLocal(String pLocal) {
		this.pLocal = pLocal;
	}
	public String getpVisitante() {
		return pVisitante;
	}
	public void setpVisitante(String pVisitante) {
		this.pVisitante = pVisitante;
	}
	public int getIdEncuentro() {
		return idEncuentro;
	}
	public void setIdEncuentro(int idEncuentro) {
		this.idEncuentro = idEncuentro;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	
	

}
